#include <iostream>
using namespace std;

int main() {
    int k = 10, j =10;

    k = k + 1;
    cout <<"o valor de k alterado para " <<k << "\n";

    j = j -1;
    cout << " o valor de j alterando para " << j << "\n";

    k++;
    cout <<"o valor de k alterado para " <<k << "\n";

    j--;
    cout << " o valor de j alterando para " << j << "\n";

    ++k;
    cout <<"o valor de k alterado para " <<k << "\n";

    --j;
    cout << " o valor de j alterando para " << j << "\n";

    return 0;


}